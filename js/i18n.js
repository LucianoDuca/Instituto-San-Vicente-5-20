function setLanguage(lang) {
  const selectedTranslations = translations[lang];

  if (!selectedTranslations) return;

  localStorage.setItem("siteLanguage", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = selectedTranslations[key];

    if (value) {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18nHtml;
    const value = selectedTranslations[key];

    if (value) {
      element.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    const value = selectedTranslations[key];

    if (value) {
      element.alt = value;
    }
  });

  document.querySelectorAll("[data-i18n-src-es][data-i18n-src-en]").forEach((image) => {
    const newSource = lang === "en"
      ? image.dataset.i18nSrcEn
      : image.dataset.i18nSrcEs;

    if (newSource) {
      image.src = newSource;
    }
  });

  const currentLanguageLabel = document.getElementById("currentLanguageLabel");

  if (currentLanguageLabel) {
    currentLanguageLabel.textContent = lang.toUpperCase();
  }
}

function initLanguageSwitcher() {
  const languageToggle = document.getElementById("languageToggle");
  const languageMenu = document.getElementById("languageMenu");
  const languageSwitcher = document.querySelector(".language-switcher");

  if (!languageToggle || !languageMenu) return;

  const savedLanguage = localStorage.getItem("siteLanguage") || "es";
  setLanguage(savedLanguage);

  languageToggle.addEventListener("click", (event) => {
    event.stopPropagation();
    languageMenu.classList.toggle("is-open");
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", (event) => {
      event.stopPropagation();

      const selectedLanguage = button.dataset.lang;
      setLanguage(selectedLanguage);

      languageMenu.classList.remove("is-open");
    });
  });

  document.addEventListener("click", (event) => {
    if (languageSwitcher && !languageSwitcher.contains(event.target)) {
      languageMenu.classList.remove("is-open");
    }
  });
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLanguageSwitcher);
} else {
  initLanguageSwitcher();
}